namespace DataRug
{
    /// <summary>
    /// Represents a stage in the psychoactivity of a substance.
    /// </summary>
    public enum SubstanceDurationStage
    {
        /// <summary>
        /// Represents the 'onset' stage of a substance.
        /// </summary>
        Onset,

        /// <summary>
        /// Represents the 'comeup' stage of a substance.
        /// </summary>
        Comeup,

        /// <summary>
        /// Represents the 'peak' stage of a substance.
        /// </summary>
        Peak,

        /// <summary>
        /// Represents the 'comedown' stage of a substance.
        /// </summary>
        Comedown,

        /// <summary>
        /// Represents the 'offset' stage of a substance.
        /// </summary>
        Offset,

        /// <summary>
        /// Represents the 'afterglow' stage of a substance.
        /// </summary>
        Afterglow,
    }

}