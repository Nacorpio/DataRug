namespace DataRug
{
    /// <summary>
    /// Represents a substance category.
    /// </summary>
    public enum SubstanceCategory
    {
        /// <summary>
        /// Signifies that the substance category is undefined.
        /// </summary>
        Undefined,

        /// <summary>
        /// Signifies that the substance is a stimulant.
        /// </summary>
        Stimulant,

        /// <summary>
        /// Signifies that the substance is a psychedelic.
        /// </summary>
        Psychedelic,

        /// <summary>
        /// Signifies that the substance is a depressant.
        /// </summary>
        Depressant,

        /// <summary>
        /// Signifies that the substance is a dissociative.
        /// </summary>
        Dissociative,

        /// <summary>
        /// Signifies that the substance is a deliriant.
        /// </summary>
        Deliriant,

        /// <summary>
        /// Signifies that the substance is a nootropic.
        /// </summary>
        Nootropic
    }

}