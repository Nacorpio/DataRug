namespace DataRug
{

    /// <summary>
    /// Represents the polarity of an effect.
    /// </summary>
    public enum EffectPolarity
    {
        /// <summary>
        /// Signifies that the effect is perceived as neutral by the host.
        /// </summary>
        Neutral,

        /// <summary>
        /// Signifies that the effect is perceived as positive by the host.
        /// </summary>
        Positive,

        /// <summary>
        /// Signifies that the effect is perceived as negative by the host.
        /// </summary>
        Negative,
    }

}